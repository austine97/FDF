float	ft_power(float a, int n)
{
	int		i;
	float	b;
	int		j;

	i = 1;
	b = a;
	if (n < 0)
		j = -n;
	else
		j = n;
	if (n == 0)
		return (1);
	while (i < j)
	{
		a = a * b;
		i++;
	}
	if (n < 0)
		return (1 / a);
	else
		return (a);
}
