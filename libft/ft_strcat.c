#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*ss1;

	ss1 = (char *)s1;
	i = 0;
	while (ss1[i] != '\0')
		i++;
	j = 0;
	while (s2[j] != '\0')
	{
		ss1[i + j] = s2[j];
		j++;
	}
	ss1[i + j] = '\0';
	return (ss1);
}
