#include "libft.h"

int		ft_is_prime(int nb)
{
	int i;
	int j;

	if (nb <= 0 || nb == 1)
		return (0);
	i = 2;
	j = 0;
	while (i * i <= nb)
	{
		j = nb % i;
		if (j == 0)
			return (0);
		else
			i++;
	}
	return (1);
}
