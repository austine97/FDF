char	*ft_strcpy(char *dst, const char *src)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (src[i])
		i++;
	while (j < i)
	{
		dst[j] = src[j];
		j++;
	}
	dst[j] = '\0';
	return (dst);
}
